#!/usr/bin/env node

const fs = require("fs");
const os = require("os");
const path = require("path");

const hubDir = path.resolve(__dirname, "..");
const workspaceRoot = path.resolve(hubDir, "..");
const launcher = path.join(hubDir, "bin", "agent-mcp.js");

function readJson(file) {
  return JSON.parse(fs.readFileSync(file, "utf8"));
}

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

// Write relative to hubDir; return absolute path written.
function writeFile(relPath, content) {
  const file = path.join(hubDir, relPath);
  ensureDir(path.dirname(file));
  fs.writeFileSync(file, content);
  return file;
}

// Write at an absolute path; return that path.
function writeAbs(absPath, content) {
  ensureDir(path.dirname(absPath));
  fs.writeFileSync(absPath, content);
  return absPath;
}

function expandPath(input) {
  if (input.startsWith("${HOME}/")) return path.join(os.homedir(), input.slice("${HOME}/".length));
  if (path.isAbsolute(input)) return input;
  return path.join(workspaceRoot, input);
}

// ─── Instruction generation ───────────────────────────────────────────────────

const INSTRUCTION_HEADER = [
  "# Generated Agent Instructions",
  "",
  "Do not edit this file directly.",
  "Edit sources in `.agent-hub/instructions/`, then run `node .agent-hub/bin/agent-sync.js`.",
  "",
].join("\n");

function collectLayerFiles(layerDef) {
  const files = [];
  for (const src of layerDef.sources || []) {
    const abs = expandPath(src);
    if (!fs.existsSync(abs)) {
      process.stderr.write(`Warning: instruction source not found: ${src}\n`);
      continue;
    }
    const stat = fs.statSync(abs);
    if (stat.isDirectory()) {
      for (const name of fs.readdirSync(abs).sort()) {
        if (name.endsWith(".md")) files.push(path.join(abs, name));
      }
    } else {
      files.push(abs);
    }
  }
  return files;
}

function sectionBlock(absFile) {
  const rel = path.relative(workspaceRoot, absFile);
  const content = fs.readFileSync(absFile, "utf8").trim();
  return `\n---\n\n# Source: ${rel}\n\n${content}\n`;
}

function buildEmbeddedSkillsSection(skillsRegistry) {
  const rows = [];
  for (const root of skillsRegistry.roots || []) {
    const abs = expandPath(root.path);
    if (!fs.existsSync(abs)) continue;
    for (const name of fs.readdirSync(abs).sort()) {
      const skillFile = path.join(abs, name, "SKILL.md");
      if (!fs.existsSync(skillFile)) continue;
      rows.push({ name, content: fs.readFileSync(skillFile, "utf8").trim() });
    }
  }
  if (rows.length === 0) return "";
  const parts = ["\n---\n\n# Available Skills\n"];
  for (const { name, content } of rows) {
    parts.push(`\n## Skill: ${name}\n\n${content}\n`);
  }
  return parts.join("\n");
}

function assembleAgent(agentConfig, layerFiles, agentProfile, skillsRegistry, profileMap) {
  const layers = profileMap[agentProfile] || [];
  const parts = [INSTRUCTION_HEADER];

  for (const layerName of layers) {
    const files = layerFiles[layerName] || [];
    for (const file of files) {
      parts.push(sectionBlock(file));
    }
  }

  if (agentConfig.agentLayer) {
    const abs = expandPath(agentConfig.agentLayer);
    if (fs.existsSync(abs)) parts.push(sectionBlock(abs));
  }

  if (agentConfig.embedSkills) {
    const section = buildEmbeddedSkillsSection(skillsRegistry);
    if (section) parts.push(section);
  }

  return parts.join("").replace(/\n{4,}/g, "\n\n\n") + "\n";
}

function generateInstructions() {
  const registry = readJson(path.join(hubDir, "registry", "instructions.json"));
  const skillsRegistry = readJson(path.join(hubDir, "registry", "skills.json"));

  const layerFiles = {};
  for (const [name, layerDef] of Object.entries(registry.layers || {})) {
    layerFiles[name] = collectLayerFiles(layerDef);
  }

  const written = [];

  for (const [, agentConfig] of Object.entries(registry.agentOutputs || {})) {
    const profile = agentConfig.profile || "default";
    const content = assembleAgent(agentConfig, layerFiles, profile, skillsRegistry, registry.agentProfiles || {});
    written.push(writeFile(agentConfig.output, content));
  }

  if (registry.canonicalOutput) {
    const allLayers = Object.keys(registry.layers || {});
    const allFiles = allLayers.flatMap((n) => layerFiles[n] || []);
    const parts = [INSTRUCTION_HEADER];
    for (const file of allFiles) parts.push(sectionBlock(file));
    written.push(writeFile(registry.canonicalOutput, parts.join("").replace(/\n{4,}/g, "\n\n\n") + "\n"));
  }

  return written;
}

// ─── Root file installation ───────────────────────────────────────────────────

function generateRootFiles() {
  const registry = readJson(path.join(hubDir, "registry", "instructions.json"));
  const written = [];

  for (const [relTarget, hubRelSource] of Object.entries(registry.installedRoots || {})) {
    const srcAbs = path.join(hubDir, hubRelSource);
    if (!fs.existsSync(srcAbs)) {
      process.stderr.write(`Warning: root file source not found: ${hubRelSource}\n`);
      continue;
    }
    const targetAbs = path.join(workspaceRoot, relTarget);
    const content = fs.readFileSync(srcAbs, "utf8");
    written.push(writeAbs(targetAbs, content));
  }

  return written;
}

// ─── Skills index ─────────────────────────────────────────────────────────────

function scanSkills() {
  const registry = readJson(path.join(hubDir, "registry", "skills.json"));
  const rows = [];
  for (const root of registry.roots) {
    const abs = expandPath(root.path);
    if (!fs.existsSync(abs)) continue;
    for (const name of fs.readdirSync(abs).sort()) {
      const skillFile = path.join(abs, name, "SKILL.md");
      if (fs.existsSync(skillFile)) {
        const first = fs.readFileSync(skillFile, "utf8").split(/\r?\n/).slice(0, 12).join(" ");
        const description = (first.match(/description:\s*>?\s*(.+?)(---|$)/) || [])[1] || "";
        rows.push({ name, skillFile, description: description.trim().replace(/\s+/g, " ") });
      }
    }
  }
  return { registry, rows };
}

function generateSkillsIndex() {
  const { registry, rows } = scanSkills();
  const lines = [
    "# Generated Skills Index",
    "",
    "Do not edit this file directly. Add skills under `.agent-hub/skills/<name>/SKILL.md`, then run `node .agent-hub/bin/agent-sync.js`.",
    "",
  ];
  if (rows.length === 0) {
    lines.push("No skills found.");
  } else {
    for (const row of rows) {
      lines.push(`- **${row.name}**: ${path.relative(workspaceRoot, row.skillFile)}${row.description ? ` — ${row.description}` : ""}`);
    }
  }
  const target = registry.agentOutputs && registry.agentOutputs.index ? registry.agentOutputs.index : "generated/skills/README.md";
  return [writeFile(target, lines.join("\n") + "\n")];
}

// ─── MCP adapter generation ───────────────────────────────────────────────────

function tomlString(value) {
  return JSON.stringify(value);
}

function mcpAdapterFor(serverName, server) {
  if (server.type === "remote") return { url: server.url };
  return { command: launcher, args: [serverName] };
}

function generateCopilot(mcpRegistry) {
  const mcpServers = {};
  for (const [name, server] of Object.entries(mcpRegistry.servers)) {
    if (server.manual) continue;
    mcpServers[name] = mcpAdapterFor(name, server);
  }
  return writeFile("generated/copilot/mcp-config.json", JSON.stringify({ mcpServers }, null, 2) + "\n");
}

function generateClaude(mcpRegistry) {
  const mcpServers = {};
  for (const [name, server] of Object.entries(mcpRegistry.servers)) {
    if (server.manual) continue;
    mcpServers[name] = mcpAdapterFor(name, server);
  }
  return writeFile("generated/claude/settings.mcp.json", JSON.stringify({ mcpServers }, null, 2) + "\n");
}

function generateOpenCode(mcpRegistry) {
  const mcp = {};
  for (const [name, server] of Object.entries(mcpRegistry.servers)) {
    if (server.manual) continue;
    if (server.type === "remote") {
      mcp[name] = { type: "remote", url: server.url, enabled: true };
    } else {
      mcp[name] = { type: "local", command: [launcher, name], enabled: true };
    }
  }
  return writeFile("generated/opencode/opencode.mcp.jsonc", JSON.stringify({ mcp }, null, 2) + "\n");
}

function generateCodex(mcpRegistry) {
  const lines = [
    "# Generated by .agent-hub/bin/agent-sync.js",
    "# Copy or include these blocks in ~/.codex/config.toml after review.",
    "",
  ];

  // Skill paths must be emitted before any TOML table header, otherwise they
  // become part of the previous table.
  const skillsRegistry = readJson(path.join(hubDir, "registry", "skills.json"));
  const skillPaths = skillsRegistry.roots
    .map((r) => expandPath(r.path))
    .filter((p) => fs.existsSync(p));
  if (skillPaths.length > 0) {
    lines.push("# Agent Hub managed skill paths. Do not edit manually.");
    lines.push(`skill_paths = [${skillPaths.map(tomlString).join(", ")}]`);
    lines.push("");
  }

  for (const [name, server] of Object.entries(mcpRegistry.servers)) {
    if (server.manual) continue;
    lines.push(`[mcp_servers.${JSON.stringify(name)}]`);
    if (server.type === "remote") {
      lines.push(`url = ${tomlString(server.url)}`);
    } else {
      lines.push(`command = ${tomlString(launcher)}`);
      lines.push(`args = [${tomlString(name)}]`);
    }
    if (server.approval && server.approval.tools) {
      for (const [toolName, mode] of Object.entries(server.approval.tools)) {
        lines.push("");
        lines.push(`[mcp_servers.${JSON.stringify(name)}.tools.${JSON.stringify(toolName)}]`);
        lines.push(`approval_mode = ${tomlString(mode)}`);
      }
    }
    lines.push("");
  }

  return writeFile("generated/codex/config.mcp.toml", lines.join("\n"));
}

function generateCursor(mcpRegistry) {
  const mcpServers = {};
  for (const [name, server] of Object.entries(mcpRegistry.servers)) {
    if (server.manual) continue;
    mcpServers[name] = mcpAdapterFor(name, server);
  }
  return writeFile("generated/cursor/mcp.json", JSON.stringify({ mcpServers }, null, 2) + "\n");
}

function generateMcpAdapters() {
  const mcpRegistry = readJson(path.join(hubDir, "registry", "mcps.json"));
  return [
    generateCopilot(mcpRegistry),
    generateClaude(mcpRegistry),
    generateOpenCode(mcpRegistry),
    generateCodex(mcpRegistry),
    generateCursor(mcpRegistry),
  ];
}

// ─── Main ─────────────────────────────────────────────────────────────────────

function main() {
  const written = [
    ...generateInstructions(),
    ...generateSkillsIndex(),
    ...generateMcpAdapters(),
    ...generateRootFiles(),
  ];
  console.log(`Generated ${written.length} files:`);
  for (const file of written) {
    const rel = file.startsWith(workspaceRoot) ? path.relative(workspaceRoot, file) : file;
    console.log(`  ${rel}`);
  }
}

main();
